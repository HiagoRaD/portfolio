                <p class="data">09 de maio de 2019</p>
                <h2>
                    Professora particular
                    não teve vínculo de emprego com academia de ginástica
                </h2>
                <div class="conteudo-wrapper">
                    <p> A Quinta Turma do Tribunal Superior do Trabalho negou a existência de vínculo de emprego entre personal trainer e
                        academia de Cachoeirinha (RS) reconhecido pelos juízos de primeiro e segundo graus. De acordo com os ministros, a
                        natureza dos serviços prestados sugere grau considerável de autonomia, de modo que não se pode presumir a subordinação,
                        necessária na caracterização da relação de emprego.
                    </p>    
                    <p> A professora de educação física ajuizou a ação em 17/12/2015 contra a dona da academia, sustentando que foi instrutora
                        de musculação na microempresa de 6/7/2011 a 2/10/2014. Ao postular o reconhecimento do vínculo de emprego, denunciou
                        contratação irregular, carteira profissional não assinada, horas extras não pagas, intervalos não concedidos,
                        vale-transporte não recebido, acúmulo de função, descontos indevidos e ocorrência de dano moral.
                    </p>
                        
                    <h4>Parceria</h4>
                        
                    <p> O juízo da 2ª Vara do Trabalho de Cachoeirinha reconheceu o vínculo de emprego em relação ao período de 6/7/2011 a
                        2/10/2014, e a dona da academia recorreu, afirmando que a relação era de "parceria", de cunho civil, na qual a
                        professora tinha seus próprios alunos, com empresa própria. Negou a existência de subordinação e disse que havia
                        responsabilidades diferentes entre as partes.
                    </p>
                            
                    <p> Apresentou documentos de cadastro da empresa da personal, na condição de microempreendedora individual, com
                        recolhimentos de INSS, e argumentou que a professora não se preocupava com despesas de água, luz, telefone, internet,
                        aluguel, aquisição e manutenção de equipamentos.
                    </p>
                        
                    <h4>Relação de emprego</h4>
                        
                    <p> Ao julgar o recurso ordinário, o Tribunal Regional manteve a decisão registrada na sentença, por entender que estava
                        caracterizada a relação de emprego, nos moldes dos artigos 2º e 3º da CLT. Conforme o TRT, não foram apresentados
                        demonstrativos de que houvesse critério de atendimento e ressarcimento diferenciado quanto a clientes direcionados ao
                        serviço de personal trainer pela professora ou divisão de responsabilidades e co-benefício especial entre as partes.
                        Assinalou que a legislação estabelece que, na ausência de fixação e demonstração de modalidade de trabalho diversa da de
                        emprego, presentes os requisitos legais e observado o ônus de prova, presume-se empregatícia a relação.
                    </p>
                        
                    <p> Salientou que a empresa relatou “diversos elementos indicadores de subordinação e de não eventualidade” e que
                        onerosidade e pessoalidade eram incontroversas. Acrescentou, ainda, não haver contrato entre as partes ou avença
                        demonstrável de relação de cunho civil. Quanto à existência de inscrição de CNPJ individual da professora, de 2012 a
                        2014, considerou não ser suficiente para comprovar que tivesse se “estabelecido e mantido relação civil de trabalho”.
                    </p>
                        
                    <h4>Personal trainer</h4>
                        
                    <p> No recurso ao TST, a dona da academia afirmou que, se a ausência de contrato demonstrando que a relação entre as partes
                        era de cunho civil “resulta na presunção de que haveria uma relação de emprego”, então, segundo ela, “a ausência de um
                        contrato de trabalho também pressuporia a inexistência de uma relação de emprego”.
                    </p>
                        
                    <p> Sustentou ter sido provado que a professora de educação física mantinha uma empresa com esse objeto social, recolhendo
                        ISS regularmente, o que permitiria “não apenas cogitar, como aferir, comprovadamente, o benefício que a profissional
                        tinha em atender seus clientes pessoais como personal trainer”. Argumentou, ainda, que o fato de ter essa empresa
                        individual, expressamente constituída para prestar serviços de condicionamento físico, no período em que alegou ter
                        mantido vínculo de emprego com a academia, “por si, é prova mais do que suficiente para comprovar que inexistia relação
                        de emprego”.
                    </p>
                        
                    <h4>Natureza dos serviços</h4>
                        
                    <p> O relator do recurso de revista, ministro Breno Medeiros, salientou não desconhecer a jurisprudência do TST de que,
                        admitida a prestação do serviço, é ônus do empregador comprovar que a relação jurídica se desenvolveu sob forma diversa
                        da empregatícia. No entanto, ele avaliou que, em situações como a presente, “sabe-se que a própria natureza dos serviços
                        prestados sugere grau considerável de autonomia, de modo que descabe o reconhecimento da subordinação por presunção”.
                    </p>
                        
                    <p> Acrescentou que há indicações no acórdão do TRT de “ser hipótese de parceria comumente estabelecida no ramo de
                        atividades físicas, no qual profissional da área, com CNPJ ativo, se beneficia de local equipado e adequado para o
                        desempenho de sua atividade de personal trainer”. Para ele, a ausência de contrato escrito não representa obstáculo ao
                        reconhecimento da relação de cunho civil.
                    </p>
